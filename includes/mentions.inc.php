  <div class="cookies">
    <h2>Mentions relatives à l'utilisation de cookies</h2>

    <p>Un cookie est un petit fichier informatique qui permet d'analyser le comportement des usagers lors de la visite d'un site internet ou de l'utilisation d'un logiciel ou d'une application mobile.</p>
    <p>Les éditeurs de sites ou d'applications qui utilisent des cookies doivent :<p>
      <p>- Informer les internautes de la finalité des cookies,</p>
      <p>- Obtenir leur consentement,</p>
      <p>- Fournir aux internautes un moyen de les refuser.</p>
      <p>- La durée de validité du consentement donné dans ce cadre est de 13 mois maximum.</p>
    </div>

    <div class="particuliers">
      <h2>Mentions propres au site du particulier</h2>

      <p>Le particulier qui crée un site internet n'est pas obligé de révéler son identité. Il doit cependant communiquer à l'hébergeur de son site les éléments qui permettront de l'identifier en cas de besoin. Ces éléments d'identification sont couverts par le secret professionnel. Mais l'hébergeur du site internet a l'obligation de les dévoiler dans le cadre d'une procédure judiciaire.</p>
      <p>Si le particulier choisit de garder l'anonymat, les seules mentions obligatoires à retrouver sur son site internet sont :</p>
      <p>- Nom, prénom, adresse et numéro de téléphone de l'hébergeur de son site internet.</p>
      <p>- Si le particulier ne souhaite pas garder l'anonymat, il doit indiquer les mentions légales le concernant et celles de l'hébergeur de son site internet.</p>
    </div>

    <div class="attention">
      <p><span>Attention</span></p> <p>: le non-respect de ces obligations peut être sanctionné d'une peine de prison pouvant aller jusqu'à 1 an et de 75 000 € d'amende.</p>
    </div>

    <div class="relatives">
      <h2>Mentions relatives à l'utilisation de données personnelles</h2>

      <p>Les sites qui utilisent des données personnelles doivent obligatoirement mentionner les informations suivantes :</p>
      <p>- Coordonnées du délégué à la protection des données (DPO ou DPD) de l'organisme, s'il a été désigné, ou d'un point de contact sur les questions de protection des données personnelles.</p>
      <p>- Finalité poursuivie par le traitement auquel les données sont destinées.</p>
      <p>- Caractère obligatoire ou facultatif des réponses et conséquences éventuelles à l'égard de l'internaute d'un défaut de réponse.</p>
      <p>- Destinataires ou catégories de destinataires des données.</p>
      <p>- Droits d'opposition, d'interrogation, d'accès et de rectification.</p>
      <p>- Au besoin, transferts de données à caractère personnel envisagés à destination d'un État n'appartenant pas à l'Union européenne.</p>
      <p>- Base juridique du traitement de données (c'est-à-dire ce qui autorise légalement le traitement : il peut s'agir du consentement des personnes concernées, du respect d'une obligation prévue par un texte, de l'exécution d'un contrat notamment).</p>
      <p>- Droit d'introduire une réclamation auprès de la Cnil</p>
    </div>

