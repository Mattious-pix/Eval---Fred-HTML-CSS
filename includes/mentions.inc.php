	<div class="cookies">
		<h2>Mentions relatives à l'utilisation de cookies</h2>

		<p class="mentcook">
			Un cookie est un petit fichier informatique qui permet d'analyser le comportement des usagers lors de la visite d'un site internet ou de l'utilisation d'un logiciel ou d'une application mobile.
		</p>

		<nav clas="cookspe">
			<ul>
				<li class="editeur"> Les éditeurs de sites ou d'applications qui utilisent des cookies doivent :</li>
				<li>Informer les internautes de la finalité des cookies,</li>
				<li>Obtenir leur consentement,</li>
				<li>Fournir aux internautes un moyen de les refuser,</li>
				<li>La durée de validité du consentement donné dans ce cadre est de 13 mois maximum.</li>
			</ul>
		</nav>
	</div>

	<div class="particuliers">
		<h2>Mentions propres au site du particulier</h2>

		<p class="parti">
			Le particulier qui crée un site internet n'est pas obligé de révéler son identité. Il doit cependant communiquer à l'hébergeur de son site les éléments qui permettront de l'identifier en cas de besoin. Ces éléments d'identification sont couverts par le secret professionnel. Mais l'hébergeur du site internet a l'obligation de les dévoiler dans le cadre d'une procédure judiciaire.
		</p>

		<nav clas="cookspe">
			<ul>
				<li class="anony"> Si le particulier choisit de garder l'anonymat, les seules mentions obligatoires à retrouver sur son site internet sont :</li>

				<li>Nom, prénom, adresse et numéro de téléphone de l'hébergeur de son site internet,</li>
				<li>Si le particulier ne souhaite pas garder l'anonymat, il doit indiquer les mentions légales le concernant et celles de l'hébergeur de son site internet.</li>
			</ul>
		</nav>
	</div>

	<div class="attention">
		<p>
			<span>Attention</span>
		</p>
		<p>
			: le non-respect de ces obligations peut être sanctionné d'une peine de prison pouvant aller jusqu'à 1 an et de 75 000 € d'amende.
		</p>
	</div>

	<div class="relatives">
		<h2>Mentions relatives à l'utilisation de données personnelles</h2>

		<nav clas="donnés">
			<ul>
				<li class="perso">Les sites qui utilisent des données personnelles doivent obligatoirement mentionner les informations suivantes :</li>
				<li>Coordonnées du délégué à la protection des données (DPO ou DPD) de l'organisme, s'il a été désigné, ou d'un point de contact sur les questions de protection des données personnelles.</li>
				<li>Finalité poursuivie par le traitement auquel les données sont destinées.</li>
				<li>Caractère obligatoire ou facultatif des réponses et conséquences éventuelles à l'égard de l'internaute d'un défaut de réponse.</li>
				<li>Destinataires ou catégories de destinataires des données.</li>
				<li>Droits d'opposition, d'interrogation, d'accès et de rectification.</li>
				<li>Au besoin, transferts de données à caractère personnel envisagés à destination d'un État n'appartenant pas à l'Union européenne.</li>
				<li>Base juridique du traitement de données (c'est-à-dire ce qui autorise légalement le traitement : il peut s'agir du consentement des personnes concernées, du respect d'une obligation prévue par un texte, de l'exécution d'un contrat notamment).</li>
				Droit d'introduire une réclamation auprès de la Cnil.</li>
			</ul>
		</nav>
